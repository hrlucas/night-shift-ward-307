/*
 * Devolve o texto correspondente ao StringID. Duas guardas defensivas: id fora
 * da faixa vira ""; e um StringID sem texto na tabela também vira "" (nunca
 * retorna NULL). Não há lógica de idioma: existe um único array de textos.
 */
const char* i18n_get(StringID id) {
    if (id < 0 || id >= STR_COUNT) {
        return "";
    }
    const char* value = strings[id];
    return (value != NULL) ? value : "";
}
